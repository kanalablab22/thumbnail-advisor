def detect_material(keyword: str) -> str:
    """キーワードから素材・特性を検出"""
    for material, kws in MATERIAL_KEYWORDS.items():
        for kw in kws:
            if kw in keyword:
                return material
    return None


def adjust_advice_for_genre(results: list, keyword: str) -> list:
    """ジャンル・素材に応じてアドバイス文を差し替える"""
    material = detect_material(keyword)
    if not material:
        genre = detect_genre(keyword)
        if genre == "食品・グルメ":
            material = "食品"
        elif genre == "バッグ・財布":
            material = "革"

    if not material:
        return results

    overrides = ADVICE_OVERRIDES.get("photo_quality", {}).get(material, {})
    if not overrides:
        return results

    adjusted = []
    for r in results:
        if r.name == "photo_quality" and r.score_value in overrides:
            override = overrides[r.score_value]
            from image_checker import CheckResult
            adjusted.append(CheckResult(
                name=r.name,
                passed=r.passed,
                value=override["t"],
                detail=override["d"],
                level=override["p"],
                score_value=r.score_value,
            ))
        else:
            adjusted.append(r)
    return adjusted


def detect_genre(keyword: str) -> str:
    """検索キーワードからジャンルを判別する"""
    keyword_lower = keyword.lower()
    best_genre = None
    best_count = 0

    for genre, keywords in GENRE_KEYWORDS.items():
        count = sum(1 for kw in keywords if kw.lower() in keyword_lower)
        if count > best_count:
            best_count = count
            best_genre = genre

    return best_genre


def get_genre_advice(keyword: str) -> dict:
    """キーワードからジャンルを判別してアドバイスを返す"""
    genre = detect_genre(keyword)
    if genre and genre in GENRE_ADVICE:
        advice = GENRE_ADVICE[genre]
        return {
            "genre": genre,
            "icon": advice["icon"],
            "reference_shops": advice["reference_shops"],
            "tips": advice["tips"],
        }
    else:
        return {
            "genre": "汎用",
            "icon": DEFAULT_ADVICE["icon"],
            "reference_shops": DEFAULT_ADVICE["reference_shops"],
            "tips": DEFAULT_ADVICE["tips"],
        }
